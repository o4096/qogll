void* QGL_get_proc_address(const char *name)//https://www.khronos.org/opengl/wiki/Load_OpenGL_Functions
{
#if defined(_WIN32)||defined(_WIN64)
	void *p= (void*)wglGetProcAddress(name);
	if(!p || (p==(void*)0x1) || (p==(void*)0x2) || (p==(void*)0x3) || (p==(void*)-1))
	{
		HMODULE module= LoadLibraryA("opengl32.dll");
		if(!module)
		{
			printf("Unable to load opengl32.dll\n");//TODO: better logging
			return NULL;
		}
		p= (void*)GetProcAddress(module, name);
		FreeLibrary(module);
	}
	return p;
#else
	return NULL;//TODO: support other platforms
#endif
}
